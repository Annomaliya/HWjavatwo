public class inkrement {
    public static void main(String[] args) {
        int a = 11;
        int b = ++a;
        int x = a++;
        int z = 8;
        int y = --z;
        int d = z--;
        System.out.println(a);
        System.out.println(b);
        System.out.println(x);
        System.out.println(z);
        System.out.println(y);
        System.out.println(d);

    }
}
